package entidades;


public class ItemLocacao {
    private int id;
    private double valor;
    private int Diaria_id;
    private int Km_id;
    private int Locacao_id;
    
    public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public double getValor() {
		return valor;
	}
	public void setValor(double valor) {
		this.valor = valor;
	}
	public int getDiaria_id() {
		return Diaria_id;
	}
	public void setDiaria_id(int diaria_id) {
		Diaria_id = diaria_id;
	}
	public int getKm_id() {
		return Km_id;
	}
	public void setKm_id(int km_id) {
		Km_id = km_id;
	}
	public int getLocacao_id() {
		return Locacao_id;
	}
	public void setLocacao_id(int locacao_id) {
		Locacao_id = locacao_id;
	}
}
    

   
